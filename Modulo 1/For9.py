print("---------------------------------------")
# RANGE: Para recorrer un conjunto de código un número específico de veces, podemos
# usar la función range ()
for x in range(6):
    print(x)
print("---------------------------------------")

#-------------------------------------------------------------------
# Función range con parámetro de inicio incrementado por defecto en 1.
for y in range(2, 6):
    print(y)
print("---------------------------------------")

#-------------------------------------------------------------------
# Función range con parámetro de inicio incrementado en 5.
for z in range(2, 30, 5):
    print(z)